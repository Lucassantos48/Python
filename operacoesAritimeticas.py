# Adição
adicao = 1 + 2 
print(adicao)

# subtração
subtracao = 1 - 2
print(subtracao)

# multiplicação
multiplicacao = 2 * 2
print(multiplicacao)

# divisão
divisao = 10 / 3
print(divisao)

# divisão inteira
divisao = 10 // 3
print(divisao)

# exponenciacao
exponenciacao = 2 ** 10
print(exponenciacao)

# modulo
modulo = 55 % 2 
print(modulo)