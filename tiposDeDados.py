# Argumentos numericos
print(12345)

# Aspas siimples
print('Lucas Marques')
print('Lucas "Marques"')

# Aspas duplas
print("Lucas Marques")
print("Lucas 'Marques'")

# Escapes
print("Lucas \"Marques\"")

# r
print(r"Lucas \"Marques\"")

# A classe type mostra o tipo que o python inferiu ao valor
print(type('Lucas Marques'))